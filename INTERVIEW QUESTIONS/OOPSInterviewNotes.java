// ==========================================
// 🧠 1. WHAT IS OOPS?
// ==========================================

// 👉 Definition:
// OOPS is a programming concept based on objects and classes

// 👉 Hinglish:
// OOPS ek approach hai jisme hum real-world objects use karke program design karte hain

// 👉 Easy English:
// OOPS is a programming concept where we design programs using objects and classes


// ==========================================
// 🧠 2. WHY OOPS?
// ==========================================

// 👉 Reason:

// ✔ Code reuse
// ✔ Easy maintenance
// ✔ Real-world modeling

// 👉 Hinglish:
// OOPS code ko reusable aur maintainable banata hai

// 👉 Easy English:
// OOPS makes code reusable and easy to maintain


// ==========================================
// 🧠 3. ABSTRACT vs INTERFACE
// ==========================================

// 👉 Abstract:
// Partial abstraction
// Can have method body
// Uses extends

// 👉 Interface:
// Full abstraction (mostly)
// No constructor
// Supports multiple inheritance

// 👉 Hinglish:
// Abstract class me implementation ho sakta hai,
// Interface sirf rules define karta hai

// 👉 Easy English:
// Abstract class can have implementation,
// Interface defines rules only


// ==========================================
// 🧠 4. OVERLOADING vs OVERRIDING
// ==========================================

// 👉 Overloading:
// Same method name, different parameters
// Compile-time

// 👉 Overriding:
// Same method, child class redefine
// Runtime

// 👉 Hinglish:
// Overloading me parameters change hote hain,
// Overriding me method redefine hota hai

// 👉 Easy English:
// Overloading = same name, different parameters
// Overriding = redefining parent method


// ==========================================
// 🧠 5. INHERITANCE vs COMPOSITION
// ==========================================

// 👉 Inheritance:
// IS-A relationship
// Uses extends

// 👉 Composition:
// HAS-A relationship
// Uses object

// 👉 Hinglish:
// Inheritance IS-A hota hai,
// Composition HAS-A hota hai

// 👉 Easy English:
// Inheritance = IS-A
// Composition = HAS-A


// ==========================================
// 🧠 6. == vs equals()
// ==========================================

// 👉 ==:
// Compares memory address (reference)

// 👉 equals():
// Compares actual values

// 👉 Hinglish:
// == address compare karta hai,
// equals() value compare karta hai

// 👉 Easy English:
// == compares reference
// equals() compares values


// ==========================================
// 🎯 INTERVIEW SHORT ANSWERS
// ==========================================

// ✔ OOPS = object-based programming
// ✔ Why OOPS = reusable + maintainable
// ✔ Overloading = same name, different parameters
// ✔ Overriding = redefine method
// ✔ Inheritance = IS-A
// ✔ Composition = HAS-A
// ✔ == = reference
// ✔ equals() = value


// ==========================================
// 🔥 KEY POINTS (REVISION)
// ==========================================

